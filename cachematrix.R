## The following functions allow caching of the inverse of a matrix

## This function creates a special matrix object in order to cache its inverse. Included are the four setter and getter functions along with the objects x and m.

makeCacheMatrix <- function(x = matrix()) {
        i <- NULL
        set <- function(y) {
                x <<- y
                i <<- NULL
        }
        get <- function() x
        setinv <- function(inv) i <<- inv
        getinv <- function() i
        list(set = set, get = get,
             setinv = setinv,
             getinv = getinv)
}


## The next function then computes the inverse of the special matrix generated from above. Also includes a conditional clause to check whether the inverse has already been calculated.

cacheSolve <- function(x, ...) {
        i <- x$getinv()
        if(!is.null(i)) {
                message("getting cached data")
                return(i)
        }
        data <- x$get()
        i <- solve(data, ...)
        x$setinv(i)
        i
}
